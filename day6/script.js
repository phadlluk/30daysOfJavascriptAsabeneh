//do while loop
let a = 0;
do {
  console.log(a);
  a++;
} while (a <= 10);

//2.Iterate 10 to 0 using for loop, do the same using while and do while loop

//for loop
for (let i = 10; i >= 0; i--) {
  console.log(i);
}

//while loop
let i = 10;
while (i >= 0) {
  console.log(i);
  i--;
}

//do while loop
let b = 10;
do {
  console.log(b);
  b--;
} while (b >= 0);
